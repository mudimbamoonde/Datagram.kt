package Calculator

// Function to add 2 numbers
fun add(num1 : Double ,num2 : Double  ) : Double {
    val result =  num1 + num2;
    return result
}

fun sub(num1 : Double ,num2 : Double  ) : Double {
    val result =  num1 -  num2;
    return result
}

fun division(num1 : Double ,num2 : Double  ) : Double {
    val result =  num1 / num2;
    return result
}